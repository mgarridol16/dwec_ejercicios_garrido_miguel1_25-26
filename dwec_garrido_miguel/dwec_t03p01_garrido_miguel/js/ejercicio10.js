/*
Investiga sobre los métodos para redondear del objeto Math: "ceil()", "floor()" y "round()".
Haz un ejemplo donde los uses y se observe la diferencia entre cada uno de ellos.
*/

//método Math.ceil() se utiliza para redondear un número hacia arriba al entero más cercano.
let num1 = Math.ceil(3.5);
console.log(num1);

//El método Math.floor() hace lo contrario del anterior redonde al numero mas abajo
let num2 = Math.floor(3.5);
console.log(num2);

//El método Math.round() lo que hace es redondear al numero entero mas cercano
let num3 = Math.round(3.5);
console.log(num3);